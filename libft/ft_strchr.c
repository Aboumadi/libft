#include"libft.h"

char	*ft_strchr(const char *str, int c)
{
	int		i;
	char	c2;

	c2 = c;
	i = 0;
	while (str[i] != c2 && str[i] != '\0')
		i++;
	if (str[i] == c2)
		return ((char *)str + i);
	return (0);
}
